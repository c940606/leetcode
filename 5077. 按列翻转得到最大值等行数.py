class Solution:
    def maxEqualRowsAfterFlips1(self, matrix):
        from collections import defaultdict
        lookup = defaultdict(int)
        for row in matrix:
            tmp1 = ""
            tmp2 = ""
            for r in row:
                tmp1 += str(r)
                tmp2 += str(1 - r)
            lookup[tmp1] += 1
            lookup[tmp2] += 1
        return max(lookup.values())

    def maxEqualRowsAfterFlips(self, matrix):
        res = 0

        def helper(col):
            ans = 0
            for i in range(len(matrix)):
                tmp = set()
                for j in range(len(matrix[0])):
                    if j in col:
                        tmp.add(1 - matrix[i][j])
                    else:
                        tmp.add(matrix[i][j])
                if len(tmp) == 1: ans += 1
            return ans

        for i in range(len(matrix)):
            # 变成全是零
            one_col = []
            # 变成全是一
            zero_col = []
            for idx, v in enumerate(matrix[i]):
                if v == 1:
                    one_col.append(idx)
                else:
                    zero_col.append(idx)
                tmp1 = helper(one_col)
                tmp2 = helper(zero_col)
                res = max(tmp1, tmp2, res)
        return res


