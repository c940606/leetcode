class UnionFind(object):
    # 初始化


    def __init__(self, size):
        self.representative = {node: node for node in range(size)}

    # 看node属于哪个分组
    # 递归写法
    def find(self, node):
        # self.representative.setdefault(node, node)
        if self.representative[node] != node:
            self.representative[node] = self.find(self.representative[node])
        return self.representative[node]

    # 非递归
    def find(self, node):
        # self.representative.setdefault(node, node)
        p = node
        while node != self.representative[node]:
            node = self.representative[node]
        while p != node:
            t = self.representative[p]
            self.representative[p] = node
            p = t
        return node

    # 把两个是一个连通分量，连接起来
    def union(self, nodex, nodey):
        self.representative[self.find(nodex)] = self.find(nodey)


class Solution:
    def maximumMinimumPath1(self, A) -> int:
        from collections import deque
        row = len(A)
        col = len(A[0])
        dp = [[0] * col for _ in range(row)]
        dp[0][0] = A[0][0]
        queue = deque()
        queue.appendleft((0, 0))
        while queue:
            i, j = queue.pop()
            cur = dp[i][j]
            for x, y in [(1, 0), (-1, 0), (0, 1), (0, -1)]:
                tmp_i = i + x
                tmp_j = j + y
                if 0 <= tmp_i < row and 0 <= tmp_j < col:
                    if dp[tmp_i][tmp_j] < min(cur, A[tmp_i][tmp_j]):
                        dp[tmp_i][tmp_j] = min(cur, A[tmp_i][tmp_j])
                        queue.appendleft((tmp_i, tmp_j))
        return dp[-1][-1]

    def maximumMinimumPath2(self, A) -> int:
        import heapq
        row = len(A)
        col = len(A[0])
        head = [(-A[0][0], 0, 0)]
        visited = set()
        visited.add((0, 0))
        while head:
            res, i, j = heapq.heappop(head)
            if i == row - 1 and j == col - 1:
                return -res
            for x, y in [(1, 0), (-1, 0), (0, 1), (0, -1)]:
                tmp_i = i + x
                tmp_j = j + y
                if 0 <= tmp_i < row and 0 <= tmp_j < col and (tmp_i, tmp_j) not in visited:
                    visited.add((tmp_i, tmp_j))
                    heapq.heappush(head, (max(res, -A[tmp_i][tmp_j]), tmp_i, tmp_j))

