class Solution:
    def invalidTransactions(self, transactions):
        from collections import defaultdict
        transactions = set(transactions)
        res = set()
        lookup = defaultdict(set)
        visited = defaultdict(set)
        for item in transactions:
            name, time, price, city = item.split(",")
            lookup[name + "," + city].add(time)
            visited[name].add(city)
        #print(visited)
        for item in transactions:
            name, time, price, city = item.split(",")
            if int(price) >= 1000:
                res.add(item)
                continue
            for c in visited[name]:
                if c == city: continue
                for t in lookup[name + "," + c]:
                    if abs(int(t) - int(time)) <= 60:
                        res.add(item)
                        break

        return list(res)


