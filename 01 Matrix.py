    def updateMatrix2(self, matrix):
        from collections import deque
        row = len(matrix)
        col = len(matrix[0])
        stack = deque()
        res = [[float("inf")] * col for _ in range(row)]
        for i in range(row):
            for j in range(col):
                if matrix[i][j] == 0:
                    res[i][j] = 0
                    stack.append((i, j))
        # print(stack)

        while stack:
            i, j = stack.pop()
            for x, y in [(-1, 0), (1, 0), (0, 1), (0, -1)]:
                tmp_i = x + i
                tmp_j = y + j
                if tmp_i < 0 or tmp_i >= row or tmp_j < 0 or tmp_j >= col or res[tmp_i][tmp_j] <= res[i][j] + 1:
                    continue
                res[tmp_i][tmp_j] = res[i][j] + 1
                stack.appendleft((tmp_i, tmp_j))
        return res

    def updateMatrix(self, matrix: List[List[int]]) -> List[List[int]]:
        import collections
        zero = collections.deque()
        row = len(matrix)
        col = len(matrix[0])

        for i in range(row):
            for j in range(col):
                if matrix[i][j] == 0:
                    zero.appendleft([i, j])
                else:
                    matrix[i][j] = float("inf")
        # print(zero)
        while zero:
            i, j = zero.pop()
            # print(i, j)
            for x, y in [[-1, 0], [1, 0], [0, 1], [0, -1]]:
                tmp_i, tmp_j = i + x, j + y
                if tmp_i < 0 or tmp_i >= row or tmp_j < 0 or tmp_j >= col or matrix[tmp_i][tmp_j] <=  matrix[i][j] + 1:
                    continue
                zero.appendleft([tmp_i, tmp_j])
                matrix[tmp_i][tmp_j] = matrix[i][j] + 1
        return matrix


