from typing import List


class Solution:
    def minTotalDistance1(self, grid: List[List[int]]) -> int:
        from collections import deque
        row = len(grid)
        col = len(grid[0])
        dis = [[0] * col for _ in range(row)]
        homes = []

        for i in range(row):
            for j in range(col):
                if grid[i][j] == 1:
                    homes.append((i, j))

        # print(homes)


        def bfs(i, j):
            queue = deque([(i, j)])
            visited = set([(i, j)])
            step = 0
            while queue:
                n = len(queue)
                for _ in range(n):
                    i, j = queue.pop()
                    dis[i][j] += step
                    for x, y in [[1, 0], [-1, 0], [0, 1], [0, -1]]:
                        tmp_i = i + x
                        tmp_j = j + y
                        if 0 <= tmp_i < row and 0 <= tmp_j < col and (tmp_i, tmp_j) not in visited:
                            visited.add((tmp_i, tmp_j))
                            queue.appendleft((tmp_i, tmp_j))
                step += 1

        for i, j in homes:
            bfs(i, j)

        return min(min(d) for d in dis)

    def minTotalDistance2(self, grid: List[List[int]]) -> int:
        one_arr = []
        row = set()
        col = set()
        res = float("inf")
        for i in range(len(grid)):
            for j in range(len(grid[0])):
                if grid[i][j] == 1:
                    one_arr.append([i, j])
                    row.add(i)
                    col.add(j)
        tmp1, tmp2 = sum(row) // len(row), sum(col) // len(col)
        # print(row, col)
        candiates = [[tmp1 - 1, tmp2], [tmp1, tmp2 - 1], [tmp1 - 1, tmp2 - 1], [tmp1, tmp2], [tmp1 + 1, tmp2],
                     [tmp1, tmp2 + 1], [tmp1 + 1, tmp2 + 1]]

        # print(candiates)

        def dis(i, j):
            ans = 0
            for x, y in one_arr:
                ans += (abs(x - i) + abs(y - j))
            return ans

        for i in [0, -1, 1]:
            for j in [0, -1, 1]:
                if 0 <= i + tmp1 < len(grid) and 0 <= j + tmp2 < len(grid[0]):
                    res = min(res, dis(i + tmp1, j + tmp2))
        return res

    def minTotalDistance(self, grid: List[List[int]]) -> int:

        row = []
        col = []

        for i in range(len(grid)):
            for j in range(len(grid[0])):
                if grid[i][j] == 1:
                    row.append(i)
                    col.append(j)
        row.sort()
        col.sort()
        res = 0

        def helper(tmp):
            nonlocal res
            left = 0
            right = len(tmp) - 1
            while left < right:
                res += (tmp[right] - tmp[left])
                right -= 1
                left += 1
        helper(row)
        helper(col)
        return res


