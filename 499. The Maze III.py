import collections
from typing import List


class Solution:
    def findShortestWay(self, maze: List[List[int]], ball: List[int], hole: List[int]) -> str:
        dis = collections.defaultdict(lambda: float("inf"))
        s_dis = collections.defaultdict(lambda: "")
        dirs = [[1, 0], [0, -1], [0, 1], [-1, 0]]
        s_dirs = "dlru"
        row, col = len(maze), len(maze[0])
        maze[hole[0]][hole[1]] = 2
        dis[(ball[0], ball[1])] = 0

        def bfs(i, j):
            queue = collections.deque([[i, j]])
            while queue:
                i, j = queue.pop()
                steps = dis[(i, j)]
                s = s_dis[(i, j)]
                for k in range(4):
                    nxt_i, nxt_j, nxt_s = i, j, s
                    while 0 <= (tmp_i := nxt_i + dirs[k][0]) < row and 0 <= (tmp_j := nxt_j + dirs[k][1]) < col and \
                            maze[tmp_i][tmp_j] != 1:
                        nxt_i, nxt_j = tmp_i, tmp_j
                        if maze[nxt_i][nxt_j] == 2: break
                    if [i, j] == [nxt_i, nxt_j]: continue
                    nxt_steps = steps + abs(i - nxt_i) + abs(j - nxt_j)
                    nxt_s += s_dirs[k]
                    if dis[(nxt_i, nxt_j)] >= nxt_steps and s_dis[(nxt_i, nxt_j)] != nxt_s:
                        if dis[(nxt_i, nxt_j)] > nxt_steps:
                            dis[(nxt_i, nxt_j)] = nxt_steps
                            s_dis[(nxt_i, nxt_j)] = nxt_s
                        else:
                            s_dis[(nxt_i, nxt_j)] = min(s_dis[(nxt_i, nxt_j)], nxt_s)
                        queue.appendleft([nxt_i, nxt_j])
            return s_dis[(hole[0], hole[1])] or "impossible"

        return bfs(*ball)


