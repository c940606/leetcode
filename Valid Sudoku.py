from collections import Counter
class Solution:
	def isValidSudoku(self, board):
		"""
		判断一个 9x9 的数独是否有效。只需要根据以下规则，验证已经填入的数字是否有效即可。
		-----
		输入:
		[
		  ["5","3",".",".","7",".",".",".","."],
		  ["6",".",".","1","9","5",".",".","."],
		  [".","9","8",".",".",".",".","6","."],
		  ["8",".",".",".","6",".",".",".","3"],
		  ["4",".",".","8",".","3",".",".","1"],
		  ["7",".",".",".","2",".",".",".","6"],
		  [".","6",".",".",".",".","2","8","."],
		  [".",".",".","4","1","9",".",".","5"],
		  [".",".",".",".","8",".",".","7","9"]
		]
		输出: true
		:type board: List[List[str]]
		:rtype: bool
		"""
		sqr = []
		clo = [[0]*9 for i in range(9)]
		# print(clo[0][0])
		for i in range(9):
			for j in range(9):
				# print(board[j][i])
				clo[i][j] = board[j][i]
				# print(clo[i][j])
				# print("-------")
		print(clo)

		for i in range(0,9,3):
			for j in range(0,9,3):
				temp = board[i][j:j+3] + board[i+1][j:j+3]+ board[i+2][j:j+3]
				sqr.append(temp)
		if self.jupde(board) and self.jupde(sqr) and self.jupde(clo):
			return True
		return False

	def jupde(self,nums):
		for item in nums:
			while "." in item:
				item.remove(".")
			if len(item) == 0:
				continue
			a = Counter(item)
			for i in a.values():
				if i != 1:
					return False
		return True


