    def maxDistance(self, grid):
        from collections import deque
        row = len(grid)
        col = len(grid[0])
        dp = [ [0] * col for _ in range(row)]
        bfs = deque()
        for i in range(row):
            for j in range(col):
                if grid[i][j] == 1:
                    bfs.appendleft((i, j))
        step = 1
        # bfs
        while bfs:
            for _ in range(len(bfs)):
                i, j = bfs.pop()
                for x, y in [[-1, 0], [1, 0], [0, 1], [0, -1]]:
                    tmp_i = i + x
                    tmp_j = j + y
                    if 0 <= tmp_i < row and 0 <= tmp_j < col and grid[tmp_i][tmp_j] == 0 \
                        and dp[tmp_i][tmp_j] == 0:
                        dp[tmp_i][tmp_j] = step
                        bfs.appendleft((tmp_i, tmp_j))
            step += 1
        res = -1
        for i in range(row):
            for j in range(col):
                if dp[i][j]:
                    res = max(res, dp[i][j])
        return res



